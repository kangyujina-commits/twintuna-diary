// ── 스트릭 반응 ──────────────────────────────────────
export function getStreakMessage(streak: number): { dog: string; cat: string } | null {
  if (streak <= 0) return null
  if (streak === 1) return {
    dog: '오늘 일기 썼다멍!! 잘했다멍!! 내일도 꼭 써달라멍!! 🐾',
    cat: '...첫날이냥. 계속 쓸 수 있냥? 두고 보겠냥. 냥.',
  }
  if (streak === 2) return {
    dog: '이틀 연속이다멍!! 완전 대단하다멍!! 계속해멍!! 🎉',
    cat: '이틀째냥. 흠. ...기대해보겠냥. 냥.',
  }
  if (streak < 7) return {
    dog: `${streak}일 연속이다멍멍!! 꼬리가 안 멈춰멍!! 최고다멍!! 🔥`,
    cat: `${streak}일째냥. ...나쁘지 않냥. 솔직히. 냥.`,
  }
  if (streak < 14) return {
    dog: `일주일 넘게 썼다멍멍멍!!! 전설이다멍!! 영원히 함께하자멍!! ⭐`,
    cat: `${streak}일이냥. ...솔직히 좀 대단하냥. 냥냥. 냥.`,
  }
  return {
    dog: `${streak}일 연속이다멍!! 내가 제일 좋아하는 인간이다멍멍멍!!! 💕`,
    cat: `...${streak}일이냥. 나 감동받은 거 아니냥. 절대냥. ...냥냥냥.`,
  }
}

// ── 오늘의 인사 ──────────────────────────────────────
export function getDayGreeting(): { dog: string; cat: string } {
  const dow = new Date().getDay()
  return DAY_GREETINGS[dow]
}
